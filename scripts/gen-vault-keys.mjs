#!/usr/bin/env node
/**
 * Vault key codegen — Phase 6 정공 (2026-05-13).
 *
 * proto/vault-keys.json → core/src/vault_keys.rs + lib/proto-gen/vault-keys.ts 자동 생성.
 *
 * 옛 산재된 vault key 문자열 literal (`'system:ai-router:enabled'` 등 frontend 분산 + Rust 매뉴얼 const)
 * single source 통합. 새 vault key 추가 = JSON 만 수정.
 *
 * 사용:
 *   npm run gen:vault       # 단독
 *   npm run gen:proto       # buf + adapter + vault 같이
 */

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const SOURCE = resolve(__dirname, '..', 'proto', 'vault-keys.json');
const RUST_OUT = resolve(__dirname, '..', 'core', 'src', 'vault_keys.rs');
const TS_OUT = resolve(__dirname, '..', 'lib', 'proto-gen', 'vault-keys.ts');

function generateRust(spec) {
  const lines = [];
  lines.push('//! Vault key constants — AUTO-GENERATED by scripts/gen-vault-keys.mjs. DO NOT EDIT.');
  lines.push('//! Single source: proto/vault-keys.json. 새 key 추가 시 본 파일 X, JSON 만 수정.');
  lines.push('//!');
  lines.push('//! Phase 6 정공 (2026-05-13) — 옛 manual Rust 하드코드 폐기.');
  lines.push('');

  for (const [key, value] of Object.entries(spec.constants)) {
    lines.push(`pub const ${key}: &str = ${JSON.stringify(value)};`);
  }
  lines.push('');

  for (const [key, value] of Object.entries(spec.prefixes)) {
    lines.push(`pub const ${key}: &str = ${JSON.stringify(value)};`);
  }
  lines.push('');

  for (const [name, def] of Object.entries(spec.defaults)) {
    if (def.doc) lines.push(`/// ${def.doc}`);
    if (def.type === 'string') {
      lines.push(`pub const ${name}: &str = ${JSON.stringify(def.value)};`);
    } else {
      lines.push(`pub const ${name}: ${def.type} = ${String(def.value)};`);
    }
  }
  lines.push('');

  for (const [fnName, tmpl] of Object.entries(spec.templates)) {
    if (tmpl.doc) lines.push(`/// ${tmpl.doc}`);
    const args = tmpl.args.map(a => `${a.name}: &${a.type === 'string' ? 'str' : a.type}`).join(', ');
    // Replace {var} with {} in template + collect arg names in order
    const argNames = tmpl.args.map(a => a.name);
    let fmt = tmpl.rust;
    for (const argName of argNames) {
      fmt = fmt.replace(`{${argName}}`, '{}');
    }
    lines.push(`pub fn ${fnName}(${args}) -> String {`);
    lines.push(`    format!(${JSON.stringify(fmt)}, ${argNames.join(', ')})`);
    lines.push('}');
    lines.push('');
  }

  return lines.join('\n');
}

function generateTs(spec) {
  const lines = [];
  lines.push('// AUTO-GENERATED by scripts/gen-vault-keys.mjs — DO NOT EDIT.');
  lines.push('// Single source: proto/vault-keys.json. 새 key 추가 시 본 파일 X, JSON 만 수정.');
  lines.push('//');
  lines.push('// Phase 6 정공 (2026-05-13) — 옛 frontend 산재 string literal 통합.');
  lines.push('');

  for (const [key, value] of Object.entries(spec.constants)) {
    lines.push(`export const ${key} = ${JSON.stringify(value)};`);
  }
  lines.push('');

  for (const [key, value] of Object.entries(spec.prefixes)) {
    lines.push(`export const ${key} = ${JSON.stringify(value)};`);
  }
  lines.push('');

  for (const [name, def] of Object.entries(spec.defaults)) {
    if (def.doc) lines.push(`/** ${def.doc} */`);
    const value = def.type === 'string' ? JSON.stringify(def.value) : String(def.value);
    lines.push(`export const ${name} = ${value};`);
  }
  lines.push('');

  // Templates → TS functions (camelCase 이름 변환)
  for (const [fnName, tmpl] of Object.entries(spec.templates)) {
    if (tmpl.doc) lines.push(`/** ${tmpl.doc} */`);
    const tsFnName = fnName.replace(/_(\w)/g, (_, c) => c.toUpperCase());
    const args = tmpl.args.map(a => `${a.tsName ?? a.name}: ${a.type}`).join(', ');
    // tmpl.ts 가 ${varName} 형식 — TS template literal
    lines.push(`export function ${tsFnName}(${args}): string {`);
    lines.push(`  return \`${tmpl.ts}\`;`);
    lines.push('}');
    lines.push('');
  }

  return lines.join('\n');
}

function main() {
  const text = readFileSync(SOURCE, 'utf8');
  const spec = JSON.parse(text);

  const rust = generateRust(spec);
  const ts = generateTs(spec);

  mkdirSync(dirname(RUST_OUT), { recursive: true });
  mkdirSync(dirname(TS_OUT), { recursive: true });
  writeFileSync(RUST_OUT, rust);
  writeFileSync(TS_OUT, ts);

  const constants = Object.keys(spec.constants).length;
  const prefixes = Object.keys(spec.prefixes).length;
  const defaults = Object.keys(spec.defaults).length;
  const templates = Object.keys(spec.templates).length;

  console.log(`[gen-vault-keys] ${constants} constants / ${prefixes} prefixes / ${defaults} defaults / ${templates} templates`);
  console.log(`[gen-vault-keys] Rust: ${RUST_OUT}`);
  console.log(`[gen-vault-keys] TS:   ${TS_OUT}`);
}

main();
